    private void BCariWaliKelasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BCariWaliKelasActionPerformed
        // TODO add your handling code here:
        String sql;
        int pilih = CBCariWaliKelas.getSelectedIndex();
        String cari = txtCariWaliKelas.getText();
        try {
            conn.close();
            Class.forName("sun.jdbc.odbc.JdbcOdbcDriver");
            conn=DriverManager.getConnection("jdbc:odbc:akademik_smppgri2bdg");
            if(pilih==0)
                sql ="Select * from wali_kelas WHERE kelas ='" +txtCariWaliKelas.getText() + "'";
            else
                sql ="Select * from wali_kelas WHERE nama_guru Like '%" +txtCariWaliKelas.getText() + "%'";

            PreparedStatement st = conn.prepareStatement(sql);
            ResultSet rs =st.executeQuery();

            hapusIsiJTable();
            int no=0;
            while (rs.next()) {
                no++;
                String kelas = rs.getString("kelas");
                String nama_guru = rs.getString("nama_guru");
                String status_guru = rs.getString("status_guru");
                String golongan_guru = rs.getString("golongan_guru");
                
                Object [] data = {no,kelas,nama_guru,status_guru,golongan_guru};
                tabModel.addRow(data);
            }

            if(tabModel.getRowCount()>0)
                JOptionPane.showMessageDialog(this,"Data Ditemukan ");
            else
                JOptionPane.showMessageDialog(this,"Data Tidak Ditemukan.. ");

        } catch (ClassNotFoundException se) {} catch (SQLException se) {}
}//GEN-LAST:event_BCariWaliKelasActionPerformed

