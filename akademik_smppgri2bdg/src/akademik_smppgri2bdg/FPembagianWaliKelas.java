public void rubahData() {

    int ok = JOptionPane.showConfirmDialog(this,
        "Anda Yakin Ingin Mengubah Data\n Dengan kelas = '" + CBKelas.getSelectedItem() +
        "'", "Konfirmasi ",JOptionPane.YES_NO_OPTION);

    if (ok == 0) {
      try {
        Class.forName("sun.jdbc.odbc.JdbcOdbcDriver");
        conn=DriverManager.getConnection("jdbc:odbc:akademik_smppgri2bdg");
        String sql ="UPDATE wali_kelas SET nama_guru = ?,status_guru = ?,golongan_guru = ? WHERE kelas = ?";
        PreparedStatement st = conn.prepareStatement(sql);
        try {
          st.setString(1, String.valueOf(txtNamaWaliKelas.getText()));
          st.setString(2, String.valueOf(txtStatusWaliKelas.getText()));
          st.setString(3, String.valueOf(txtGolonganWaliKelas.getText()));
          st.setString(4, String.valueOf(CBKelas.getSelectedItem()));
          st.executeUpdate();

        tampilDataKeJTable();

        CBKelas.setSelectedItem("");
        txtNamaWaliKelas.setText("");
        txtStatusWaliKelas.setText("");
        txtGolonganWaliKelas.setText("");
        } catch (SQLException se) { }
      } catch (ClassNotFoundException se) {}
      catch (SQLException se) {}
    }

  }

